/*
    OPERADORES DE COMPARAÇÃO:
    > Maior que
    >= Maior que ou igual a
    < Menor que
    <= Menor que ou igual a
    == igualdade (valor)
    === igualdade estrita (valor e tipo)
    != Diferente (valor)
    !== diferente estrito (valor e tipo)
*/
console.log(10 > 5)
console.log(10 >= 10)
console.log(10 < 5)
console.log(10 <= 10)
console.log(10 == 10)
console.log(10 === 5*3)
console.log(10 != 5)
console.log(10 !== '10')



